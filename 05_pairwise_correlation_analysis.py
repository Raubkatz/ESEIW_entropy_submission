def interpret_correlation(value: float) -> str:
    abs_v = abs(value)
    if abs_v < 0.1:
        strength = "negligible"
    elif abs_v < 0.3:
        strength = "weak"
    elif abs_v < 0.5:
        strength = "moderate"
    elif abs_v < 0.7:
        strength = "substantial"
    else:
        strength = "strong"
    direction = "positive" if value > 0 else "negative" if value < 0 else "no"
    return f"{strength} {direction} association"


def write_report(
    path: Path,
    input_csv: Path,
    out_dir: Path,
    feature_1: str,
    feature_2: str,
    x_info: Dict,
    y_info: Dict,
    x_summary: Dict,
    y_summary: Dict,
    corr: Dict,
    fit: Dict,
    bootstrap: Optional[Dict],
    plot_paths: Dict[str, Dict[str, str]],
    samplewise_paths: Dict[str, str],
    rows_raw: int,
    rows_used: int,
) -> None:
    lines: List[str] = []
    lines.append("PAIRWISE CORRELATION ANALYSIS REPORT")
    lines.append("====================================")
    lines.append("")
    lines.append(f"INPUT_CSV: {input_csv.resolve()}")
    lines.append(f"OUTPUT_FOLDER: {out_dir.resolve()}")
    lines.append("")
    lines.append(f"FEATURE_1_X: {feature_1}")
    lines.append(f"FEATURE_2_Y: {feature_2}")
    lines.append(f"ROWS_IN_INPUT: {rows_raw}")
    lines.append(f"ROWS_USED_PAIRWISE_COMPLETE: {rows_used}")
    lines.append(f"ROWS_DROPPED_PAIRWISE: {rows_raw - rows_used}")
    lines.append("")

    lines.append("SAMPLE-BY-SAMPLE HANDLING")
    lines.append("-------------------------")
    lines.append("Yes: the script treats one original CSV row as one sample.")
    lines.append("For every sample, it collects FEATURE_1 raw value, FEATURE_2 raw value, converted x, converted y, and a pairwise_complete flag.")
    lines.append("The statistical analysis uses the pairwise-complete rows only when DROP_MISSING_PAIRWISE=True.")
    lines.append("The sample-wise CSV exports are sorted according to SAMPLEWISE_SORT_BY.")
    if samplewise_paths:
        for key, value in samplewise_paths.items():
            lines.append(f"{key}: {value}")
    else:
        lines.append("Sample-wise table export is disabled.")
    lines.append("")

    lines.append("COLUMN CONVERSION")
    lines.append("-----------------")
    for label, info in [("FEATURE_1_X", x_info), ("FEATURE_2_Y", y_info)]:
        lines.append(f"{label}: {info['column']}")
        lines.append(f"  original_dtype: {info['original_dtype']}")
        lines.append(f"  conversion_kind: {info['conversion_kind']}")
        lines.append(f"  encoding: {info['encoding']}")
        lines.append(f"  raw_missing: {info['num_missing_raw']}")
        lines.append(f"  raw_unique_non_missing: {info['num_unique_raw_non_missing']}")
        if "num_category_levels" in info:
            lines.append(f"  category_levels: {info['num_category_levels']}")
            lines.append("  category_code_mapping_first_levels:")
            for k, v in info.get("category_code_mapping_first_levels", {}).items():
                lines.append(f"    {v}: {k}")
            if info.get("category_mapping_truncated"):
                lines.append(f"  note: {info.get('category_mapping_note')}")
        lines.append("")

    lines.append("NUMERIC SUMMARIES AFTER CONVERSION")
    lines.append("----------------------------------")
    for name, summary in [(feature_1, x_summary), (feature_2, y_summary)]:
        lines.append(name)
        for k, v in summary.items():
            lines.append(f"  {k}: {v}")
        lines.append("")

    lines.append("CORRELATION RESULTS")
    lines.append("-------------------")
    pear = corr["pearson"]
    spear = corr["spearman"]
    kend = corr["kendall"]
    lines.append(f"Pearson r:   {pear['r']:.6f}    p-value: {pear['p_value']:.6g}    interpretation: {interpret_correlation(pear['r'])}")
    lines.append(f"Spearman rho:{spear['rho']:.6f}    p-value: {spear['p_value']:.6g}    interpretation: {interpret_correlation(spear['rho'])}")
    lines.append(f"Kendall tau: {kend['tau']:.6f}    p-value: {kend['p_value']:.6g}    interpretation: {interpret_correlation(kend['tau'])}")
    lines.append("")

    lines.append("LINEAR FIT")
    lines.append("----------")
    lines.append(f"Model: {feature_2} = intercept + slope * {feature_1}")
    lines.append(f"intercept: {fit['intercept']:.10g}")
    lines.append(f"slope: {fit['slope']:.10g}")
    lines.append(f"slope_std_error: {fit['slope_std_error']:.10g}")
    lines.append(f"slope_p_value: {fit['linear_regression_p_value_for_slope']:.6g}")
    lines.append(f"R_squared: {fit['r_squared']:.6f}")
    lines.append(f"adjusted_R_squared: {fit['adjusted_r_squared']:.6f}")
