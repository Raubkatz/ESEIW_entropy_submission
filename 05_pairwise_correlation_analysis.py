        lines.append("------------------------------")
        lines.append(f"CI level: {bootstrap['ci_level']['value']}")
        for key in ["slope", "intercept", "r_squared"]:
            b = bootstrap[key]
            lines.append(f"{key}: mean={b['mean']:.10g}, low={b['low']:.10g}, high={b['high']:.10g}, successful_iterations={b['n_successful']}")
        lines.append("")

