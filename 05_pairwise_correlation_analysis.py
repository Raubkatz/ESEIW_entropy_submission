def dtype_kind(series: pd.Series) -> str:
    if pd.api.types.is_bool_dtype(series):
        return "boolean"
    if pd.api.types.is_numeric_dtype(series):
        return "numeric"
    if pd.api.types.is_datetime64_any_dtype(series):
        return "datetime"
    return "categorical_or_text"


def coerce_series_for_analysis(series: pd.Series, max_levels: int = MAX_CATEGORY_LEVELS_IN_REPORT) -> Tuple[pd.Series, Dict]:
    """
    Convert any selected CSV column into a numeric analysis vector.

    Plain meaning:
    - numeric columns stay numeric
    - bool columns become 0/1
    - datetime columns become seconds since epoch
    - object/string/category columns become deterministic category codes
    """
    original_dtype = str(series.dtype)
    kind = dtype_kind(series)
    info: Dict = {
        "column": series.name,
        "original_dtype": original_dtype,
        "conversion_kind": kind,
        "encoding": None,
        "num_unique_raw_non_missing": int(series.dropna().nunique()),
        "num_missing_raw": int(series.isna().sum()),
    }

    if kind == "boolean":
        out = series.astype("float64")
        info["encoding"] = "boolean converted to 0/1"
        return out, info

    if kind == "numeric":
        out = pd.to_numeric(series, errors="coerce")
        info["encoding"] = "numeric conversion with errors coerced to NaN"
        return out, info

    if kind == "datetime":
        dt = pd.to_datetime(series, errors="coerce")
        out = dt.astype("int64") / 1e9
        out = pd.Series(out, index=series.index, name=series.name).replace({-9223372036.854776: np.nan})
        info["encoding"] = "datetime converted to Unix seconds"
        return out, info

    # Try numeric first for object columns that contain numeric strings.
    numeric_try = pd.to_numeric(series, errors="coerce")
    non_missing = series.notna().sum()
    converted_non_missing = numeric_try.notna().sum()
    if non_missing > 0 and converted_non_missing / non_missing >= 0.95:
        info["conversion_kind"] = "numeric_string"
        info["encoding"] = "object/string parsed as numeric because >=95% of non-missing values were numeric"
        return numeric_try, info

    # Categorical/text fallback.
    cat = series.astype("string").fillna("<MISSING>").astype("category")
    out = pd.Series(cat.cat.codes.astype("float64"), index=series.index, name=series.name)
    levels = list(cat.cat.categories)
    mapping = {str(level): int(code) for code, level in enumerate(levels[:max_levels])}
    info["conversion_kind"] = "categorical_or_text_encoded"
    info["encoding"] = "category codes assigned by pandas category order; <MISSING> is explicit before pairwise dropping"
    info["num_category_levels"] = int(len(levels))
    info["category_code_mapping_first_levels"] = mapping
    if len(levels) > max_levels:
        info["category_mapping_truncated"] = True
        info["category_mapping_note"] = f"Only first {max_levels} levels included in report/JSON."
    else:
        info["category_mapping_truncated"] = False
    return out, info


def basic_summary(x: np.ndarray) -> Dict[str, float]:
    return {
        "n": int(len(x)),
        "mean": float(np.mean(x)),
        "std": float(np.std(x, ddof=1)) if len(x) > 1 else float("nan"),
        "min": float(np.min(x)),
        "q25": float(np.quantile(x, 0.25)),
        "median": float(np.median(x)),
        "q75": float(np.quantile(x, 0.75)),
        "max": float(np.max(x)),
    }


def corr_with_pvalues(x: np.ndarray, y: np.ndarray) -> Dict[str, Dict[str, float]]:
    out: Dict[str, Dict[str, float]] = {}
    pearson = stats.pearsonr(x, y)
    spearman = stats.spearmanr(x, y, nan_policy="omit")
    kendall = stats.kendalltau(x, y, nan_policy="omit")
    out["pearson"] = {"r": float(pearson.statistic), "p_value": float(pearson.pvalue)}
    out["spearman"] = {"rho": float(spearman.statistic), "p_value": float(spearman.pvalue)}
    out["kendall"] = {"tau": float(kendall.statistic), "p_value": float(kendall.pvalue)}
    return out


def linear_fit_metrics(x: np.ndarray, y: np.ndarray) -> Dict[str, float | List[float]]:
    n = len(x)
    slope, intercept, r_value, p_value, std_err = stats.linregress(x, y)
    fitted = intercept + slope * x
    residuals = y - fitted
    sse = float(np.sum(residuals ** 2))
    mse = float(np.mean(residuals ** 2))
    rmse = float(math.sqrt(mse))
    mae = float(np.mean(np.abs(residuals)))
    y_var = float(np.sum((y - np.mean(y)) ** 2))
    r2 = float(1.0 - sse / y_var) if y_var > 0 else float("nan")
    adj_r2 = float(1.0 - (1.0 - r2) * (n - 1) / (n - 2)) if n > 2 and np.isfinite(r2) else float("nan")
    residual_se = float(math.sqrt(sse / (n - 2))) if n > 2 else float("nan")

    resid_std = float(np.std(residuals, ddof=1)) if n > 1 else float("nan")
    standardized_resid = residuals / resid_std if resid_std > 0 else np.full_like(residuals, np.nan)
    outlier_2sd = int(np.sum(np.abs(standardized_resid) > 2)) if resid_std > 0 else 0
    outlier_3sd = int(np.sum(np.abs(standardized_resid) > 3)) if resid_std > 0 else 0

    return {
        "n": int(n),
        "slope": float(slope),
        "intercept": float(intercept),
        "linear_regression_p_value_for_slope": float(p_value),
        "slope_std_error": float(std_err),
        "r_value": float(r_value),
        "r_squared": float(r2),
        "adjusted_r_squared": float(adj_r2),
        "rmse": rmse,
        "mae": mae,
        "sse": sse,
        "mse": mse,
        "residual_standard_error": residual_se,
        "residual_mean": float(np.mean(residuals)),
        "residual_std": resid_std,
        "residual_min": float(np.min(residuals)),
        "residual_q25": float(np.quantile(residuals, 0.25)),
        "residual_median": float(np.median(residuals)),
        "residual_q75": float(np.quantile(residuals, 0.75)),
        "residual_max": float(np.max(residuals)),
        "standardized_residuals_abs_gt_2": outlier_2sd,
        "standardized_residuals_abs_gt_3": outlier_3sd,
        "fitted_values": fitted.tolist(),
        "residuals": residuals.tolist(),
    }


def bootstrap_linear_ci(x: np.ndarray, y: np.ndarray, n_iter: int, ci: float, random_state: int) -> Dict[str, Dict[str, float]]:
    rng = np.random.default_rng(random_state)
    n = len(x)
    slopes: List[float] = []
    intercepts: List[float] = []
    r2s: List[float] = []

    for _ in range(n_iter):
        idx = rng.integers(0, n, size=n)
        xb = x[idx]
        yb = y[idx]
        if np.nanstd(xb) <= LOW_VARIANCE_EPS or np.nanstd(yb) <= LOW_VARIANCE_EPS:
            continue
        try:
            m = linear_fit_metrics(xb, yb)
        except Exception:
            continue
        slopes.append(float(m["slope"]))
        intercepts.append(float(m["intercept"]))
        r2s.append(float(m["r_squared"]))

    alpha = (1.0 - ci) / 2.0
    lo_q = alpha
    hi_q = 1.0 - alpha

    def q(vals: List[float]) -> Dict[str, float]:
        arr = np.asarray(vals, dtype=float)
        arr = arr[np.isfinite(arr)]
        if len(arr) == 0:
            return {"low": float("nan"), "high": float("nan"), "mean": float("nan"), "n_successful": 0}
        return {
            "low": float(np.quantile(arr, lo_q)),
            "high": float(np.quantile(arr, hi_q)),
            "mean": float(np.mean(arr)),
            "n_successful": int(len(arr)),
        }

    return {
        "ci_level": {"value": float(ci)},
        "slope": q(slopes),
        "intercept": q(intercepts),
        "r_squared": q(r2s),
    }


def prepare_analysis_dataframe(df: pd.DataFrame, feature_1: str, feature_2: str) -> Tuple[pd.DataFrame, pd.DataFrame, Dict, Dict]:
    """
    Build the row-wise pair table.

    Plain meaning:
    - One original CSV row is one sample.
    - For every sample, we collect FEATURE_1 and FEATURE_2 raw values.
    - We also store the converted numeric values x and y used by the analysis.
    - Then, only if DROP_MISSING_PAIRWISE=True, incomplete rows are removed from the
      statistical analysis table. The full sample-wise table is still saved separately.
    """
    if feature_1 not in df.columns:
        raise SystemExit(f"FEATURE_1 not found in CSV: {feature_1}\nAvailable columns include:\n" + "\n".join(df.columns[:100]))
    if feature_2 not in df.columns:
        raise SystemExit(f"FEATURE_2 not found in CSV: {feature_2}\nAvailable columns include:\n" + "\n".join(df.columns[:100]))

    x_num, x_info = coerce_series_for_analysis(df[feature_1])
    y_num, y_info = coerce_series_for_analysis(df[feature_2])

    # Explicit sample-by-sample collection. This is the central table: one row = one sample.
    samplewise_all = pd.DataFrame({
        "original_row_index": df.index.astype(int),
        "feature_1_name": feature_1,
        "feature_2_name": feature_2,
        "feature_1_raw": df[feature_1],
        "feature_2_raw": df[feature_2],
        "x": x_num,
        "y": y_num,
    })
    samplewise_all["pairwise_complete"] = samplewise_all["x"].notna() & samplewise_all["y"].notna()

    rows_before = len(samplewise_all)
    if DROP_MISSING_PAIRWISE:
        out = samplewise_all.loc[samplewise_all["pairwise_complete"]].copy()
    else:
        out = samplewise_all.copy()
    rows_after = len(out)

    x_info["rows_before_pairwise_drop"] = int(rows_before)
    x_info["rows_after_pairwise_drop"] = int(rows_after)
    y_info["rows_before_pairwise_drop"] = int(rows_before)
    y_info["rows_after_pairwise_drop"] = int(rows_after)

    return out, samplewise_all, x_info, y_info


def sort_samplewise_dataframe(df: pd.DataFrame) -> pd.DataFrame:
    """Sort the sample-wise output using the configured columns."""
    sort_cols = [c for c in SAMPLEWISE_SORT_BY if c in df.columns]
    if not sort_cols:
        return df.copy()

    ascending = SAMPLEWISE_SORT_ASCENDING[:len(sort_cols)]
    if len(ascending) < len(sort_cols):
        ascending = ascending + [True] * (len(sort_cols) - len(ascending))

    return df.sort_values(sort_cols, ascending=ascending, kind="mergesort").reset_index(drop=True)


def add_samplewise_diagnostics(adf: pd.DataFrame, fit: Optional[Dict] = None) -> pd.DataFrame:
    """
    Add row-wise diagnostics to the complete sample table.

    Plain meaning:
    - rank_x and rank_y show the order of each sample after sorting/ranking.
    - fitted_y and residual are only available after the linear model was fitted.
    """
    out = adf.copy()

    if SAMPLEWISE_ADD_RANK_COLUMNS:
        out["rank_x"] = out["x"].rank(method="average")
        out["rank_y"] = out["y"].rank(method="average")

    if fit is not None:
        out["fitted_y"] = np.asarray(fit["fitted_values"], dtype=float)
        out["residual"] = np.asarray(fit["residuals"], dtype=float)
        resid_std = float(np.nanstd(out["residual"].to_numpy(dtype=float), ddof=1)) if len(out) > 1 else float("nan")
        if np.isfinite(resid_std) and resid_std > 0:
            out["standardized_residual"] = out["residual"] / resid_std
            out["abs_standardized_residual"] = out["standardized_residual"].abs()
        else:
            out["standardized_residual"] = np.nan
            out["abs_standardized_residual"] = np.nan

    return out


def save_samplewise_tables(
    out_dir: Path,
    pair_name: str,
    samplewise_all: pd.DataFrame,
    analysis_complete: pd.DataFrame,
) -> Dict[str, str]:
    """Save explicit sample-by-sample CSV files."""
    paths: Dict[str, str] = {}
    if not SAVE_SAMPLEWISE_TABLES:
        return paths

    samplewise_dir = out_dir / "samplewise_tables"
    ensure_dir(samplewise_dir)

    all_rows_path = samplewise_dir / f"{pair_name}_samplewise_all_rows.csv"
    samplewise_all_sorted = sort_samplewise_dataframe(samplewise_all)
    samplewise_all_sorted.to_csv(all_rows_path, index=False)
    paths["samplewise_all_rows_sorted_csv"] = str(all_rows_path.resolve())

    complete_path = samplewise_dir / f"{pair_name}_samplewise_pairwise_complete_sorted.csv"
    complete_sorted = sort_samplewise_dataframe(analysis_complete)
    complete_sorted.to_csv(complete_path, index=False)
    paths["samplewise_pairwise_complete_sorted_csv"] = str(complete_path.resolve())

    residual_sorted = analysis_complete.copy()
    if "abs_standardized_residual" in residual_sorted.columns:
        residual_sorted = residual_sorted.sort_values("abs_standardized_residual", ascending=False, kind="mergesort")
        residual_path = samplewise_dir / f"{pair_name}_samplewise_sorted_by_residual_outlier_strength.csv"
        residual_sorted.to_csv(residual_path, index=False)
        paths["samplewise_sorted_by_residual_outlier_strength_csv"] = str(residual_path.resolve())

    return paths

def sample_for_plot(adf: pd.DataFrame) -> pd.DataFrame:
    if MAX_POINTS_FOR_SCATTER_PLOTS is None or len(adf) <= MAX_POINTS_FOR_SCATTER_PLOTS:
        return adf
    return adf.sample(n=MAX_POINTS_FOR_SCATTER_PLOTS, random_state=RANDOM_STATE)


def add_regression_equation(ax, metrics: Dict[str, float], loc: str = "upper left") -> None:
    txt = (
        f"y = {metrics['intercept']:.4g} + {metrics['slope']:.4g} x\n"
        f"R² = {metrics['r_squared']:.4f}\n"
        f"RMSE = {metrics['rmse']:.4g}"
    )
    anchor = (0.03, 0.97) if loc == "upper left" else (0.97, 0.03)
    ha = "left" if loc == "upper left" else "right"
    va = "top" if loc == "upper left" else "bottom"
    ax.text(
        anchor[0], anchor[1], txt,
        transform=ax.transAxes,
        ha=ha,
        va=va,
        bbox=dict(facecolor="white", edgecolor="0.7", alpha=0.85),
        fontsize=12 * FONT_SCALE,
    )


def make_plots(adf: pd.DataFrame, out_dir: Path, feature_1: str, feature_2: str, metrics: Dict[str, float]) -> Dict[str, Dict[str, str]]:
    plot_paths: Dict[str, Dict[str, str]] = {}
    plot_df = sample_for_plot(adf)
    x = adf["x"].to_numpy(dtype=float)
    y = adf["y"].to_numpy(dtype=float)
    fitted = np.asarray(metrics["fitted_values"], dtype=float)
    residuals = np.asarray(metrics["residuals"], dtype=float)

    # 1) Scatter + linear fit.
    plt.figure(figsize=(10, 7))
    ax = sns.regplot(
        data=plot_df,
        x="x",
        y="y",
        scatter_kws={"s": 18, "alpha": 0.45, "color": custom_palette[0], "edgecolor": "none"},
        line_kws={"color": custom_palette[1], "linewidth": 2.5},
        ci=95,
    )
    ax.set_title(f"Linear relationship: {feature_1} vs {feature_2}")
    ax.set_xlabel(feature_1)
    ax.set_ylabel(feature_2)
    add_regression_equation(ax, metrics)
    plt.tight_layout()
    plot_paths["scatter_linear_fit"] = save_current_figure(out_dir, "01_scatter_linear_fit")

    # 2) Scatter + fit + binned means.
    tmp = adf[["x", "y"]].copy()
    tmp["bin"] = pd.qcut(tmp["x"], q=min(N_BINS, tmp["x"].nunique()), duplicates="drop")
    binned = tmp.groupby("bin", observed=True).agg(x_mean=("x", "mean"), y_mean=("y", "mean"), n=("y", "size")).reset_index()
    plt.figure(figsize=(10, 7))
    ax = plt.gca()
    ax.scatter(plot_df["x"], plot_df["y"], s=15, alpha=0.35, color=custom_palette[0], edgecolors="none", label="Rows")
    x_line = np.linspace(np.min(x), np.max(x), 200)
    y_line = metrics["intercept"] + metrics["slope"] * x_line
    ax.plot(x_line, y_line, color=custom_palette[1], linewidth=2.5, label="Linear fit")
    ax.plot(binned["x_mean"], binned["y_mean"], marker="o", color=custom_palette[2], linewidth=2.0, label="Binned means")
    ax.set_title(f"Linear fit with binned means: {feature_1} vs {feature_2}")
    ax.set_xlabel(feature_1)
    ax.set_ylabel(feature_2)
    ax.legend()
    add_regression_equation(ax, metrics)
    plt.tight_layout()
