"""
06_pairwise_correlation_analysis.py

Standalone pairwise correlation-analysis script for the cleaned target-specific CSV workflow.

What this script does:
- Loads one target-specific cleaned dataset CSV.
- Builds an explicit sample-by-sample table first: one row from the CSV becomes one row in the analysis table.
- Saves both all-row and pairwise-complete sorted sample tables before/alongside the statistical outputs.
- Lets you select FEATURE_1 and FEATURE_2 directly in this file.
- Allows every CSV column to be selected, including class/label columns.
- Converts selected columns into analysis-ready numeric vectors:
    * numeric / boolean columns are converted directly
    * string/object/category columns are encoded as category codes
- Computes a detailed pairwise relationship analysis:
    * Pearson correlation
    * Spearman rank correlation
    * Kendall rank correlation
    * simple linear regression y = intercept + slope * x
    * R², adjusted R², RMSE, MAE, residual standard error
    * residual summary and outlier counts
    * optional bootstrap confidence interval for slope/intercept/R²
- Saves several plots in both PNG and EPS:
    * scatter + linear fit
    * scatter + linear fit with binned means
    * residuals vs fitted values
    * residual histogram
    * Q-Q plot of residuals
    * observed vs fitted values
    * hexbin density plot
    * rank-rank scatter plot
- Writes a human-readable TXT report.
- Writes a machine-readable JSON summary.
- Saves all outputs into a folder named after the selected feature pair.

Run:
  python 06_pairwise_correlation_analysis.py

Adjust all parameters in the CONFIG section below.
No argument parsing is used.
"""

